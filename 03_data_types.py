# Numeric
# Integer
# Float
# Complex Number
# Sequence Type
# String
# List(val, val, val)
# Tuple(val, val, val)
# Dictionary{key:value)
# Set {value}

# Interger
num1=10
print(num1,type(num1))

# Float
num1=20
print(num1)
num2=12.8
print(num2, type(num2))

# Complex
num3=10j
print(num3, type(num3))

# String
demoStr="Hello World"
print(demoStr,type(demoStr))

# List
student=['Ram', 'Shyam', 'Radha']
print(student,type(student))
print(student[2])

# Tuple
studentDetails=('Ram', 'Shyam', 'Radha')
print(studentDetails,type(studentDetails))
print(studentDetails[0])

#  Dictionary
employee={
    "name": "Abhi",
    "age":32,
    "salary":80000,
    "scale":17
}
print(employee, type(employee))
print(employee["name"])

# Set
employeeData={
    "Ram", 32, 80000,17
}
print(employeeData,type(employeeData))